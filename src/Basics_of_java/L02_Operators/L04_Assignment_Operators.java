package Basics_of_java.L02_Operators;

public class L04_Assignment_Operators {
    static void main(String[] args) {
        int a=10;
        System.out.println(a);
        a += 10;
        System.out.println(a);
        a -= 10;
        System.out.println(a);
        a *= 10;
        System.out.println(a);
        a /= 10;
        System.out.println(a);
    }
}
